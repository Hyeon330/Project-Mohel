package com.finalproject.mohel.vo;

public class MyFoodVO {
	private int no;
	private String nickname;
	private String date;
	private String foodlist;
	private String foodcodes;
	private String foodcalories;
	private int meals;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getFoodlist() {
		return foodlist;
	}
	public void setFoodlist(String foodlist) {
		this.foodlist = foodlist;
	}
	public String getFoodcodes() {
		return foodcodes;
	}
	public void setFoodcodes(String foodcodes) {
		this.foodcodes = foodcodes;
	}
	public String getFoodcalories() {
		return foodcalories;
	}
	public void setFoodcalories(String foodcalories) {
		this.foodcalories = foodcalories;
	}
	public int getMeals() {
		return meals;
	}
	public void setMeals(int meals) {
		this.meals = meals;
	}
	
}
